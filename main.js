let navHeight = 0;
if (window.width > 1440) {
    navHeight = 75;
} else {
    navHeight = 55;
}

$('#header .title .button a').click(function (event) {
    event.preventDefault();
    let link = $(this).attr("href");
    $('html, body').animate({
        scrollTop: $(link).offset().top - navHeight
    }, 800);
})
